// 辅助函数：获取课程信息
export const getCourseInfo = (categoryId, courseId) => {
  const category = directory.categories[categoryId];
  if (!category) return null;

  const course = category.courses[courseId];
  if (!course) return null;

  return {
    ...course,
    categoryName: category.name,
    categoryNameTw: category.nameTw
  };
};

// 辅助函数：获取目录树
export const getDirectoryTree = () => {
  return Object.entries(directory.categories).map(([categoryId, category]) => ({
    id: categoryId,
    name: category.name,
    nameTw: category.nameTw,
    children: Object.entries(category.courses).map(([courseId, course]) => ({
      id: courseId,
      name: course.title,
      nameTw: course.titleTw,
      files: course.files
    }))
  }));
};

// 辅助函数：获取分类列表
export const getCategories = () => {
  return Object.entries(directory.categories).map(([id, category]) => ({
    id,
    name: category.name,
    nameTw: category.nameTw
  }));
};

// 辅助函数：获取分类下的课程
export const getCoursesByCategory = (categoryId) => {
  const category = directory.categories[categoryId];
  if (!category) return [];

  return Object.entries(category.courses).map(([id, course]) => ({
    id,
    ...course
  }));
};